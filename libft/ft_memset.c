#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = str;
	while (n != 0)
	{
		ptr[i] = c;
		n--;
		i++;
	}
	return (ptr);
}
